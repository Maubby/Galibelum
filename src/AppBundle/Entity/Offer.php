    /**
     *
     * @var string
     *
     * @ORM\Column(name="nameCanonical", type="string", length=32, nullable=true)
     */
    private $nameCanonical;

    /**
     *
     * @var string
     *
     * @ORM\Column(name="description", type="text")
     *
     * @Assert\NotBlank(
     *     message= "Veuillez détailler votre offre"
     * )
     * @Assert\Length(
     *      min = 16,
     *      max = 250,
     *      minMessage = "Votre description doit contenir au minimum {{ limit }} caractères",
     *      maxMessage = "Votre description doit contenir au maximum {{ limit }} caractères"
     * )
     */
    private $description;

    /**
     *
     * @var \DateTime
     *
     * @ORM\Column(name="date", type="date")
     *
     * @Assert\NotBlank(
     *     message="Veuillez saisir une date"
     * )
     * @Assert\Date()
     */
    private $date;

    /**
     *
     * @var int
     *
     * @ORM\Column(name="amount", type="integer")
     *
     * @Assert\Type(
     *     type="integer",
     *     message="The value {{ value }} is not a valid {{ type }}."
     * )
     *
     * @Assert\Range(
     *     min = 200,
     *     minMessage = "Veuillez saisir un montant minimal de 200€."
     * )
     */
    private $amount;

    /**
     *
     * @var int
     *
     * @ORM\Column(name="handlingFee", type="integer", nullable=true)
     */
    private $handlingFee;

    /**
     *
     * @var int
     *
     * @ORM\Column(name="partnershipNumber", type="integer")
     *
     * @Assert\Type(
     *     type="integer",
     *     message="The value {{ value }} is not a valid {{ type }}."
     * )
     * @Assert\Range(
     *      min = 1,
     *      minMessage = "Le nombre de partenaire(s) ne peut être inférieur à {{ limit }}"
     * )
     */
    private $partnershipNumber;

    /**
     *
     * @var int
     *
     * @ORM\Column(name="finalDeal", type="integer", nullable=true)
     */
    private $finalDeal;

    /**
     *
     * @var int
     *
     * @ORM\Column(name="status", type="integer")
     */
    private $status;

    /**
     *
     * @var bool
     *
     * @ORM\Column(name="signature", type="boolean", nullable=true)
     */
    private $signature;

    /*
     * Add Personal Method
     */

    /**
     * Constructor
     */
    public function __construct()
    {
        $this->setStatus(0);
        $this->setCreationDate(new \DateTime());
    }

    /*
     * Autogenerated methods
     */

    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Offer
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set nameCanonical
     *
     * @param string $nameCanonical
     *
     * @return Offer
     */
    public function setNameCanonical($nameCanonical)
