        $this->nameCanonical = $nameCanonical;

        return $this;
    }

    /**
     * Get nameCanonical
     *
     * @return string
     */
    public function getNameCanonical()
    {
        return $this->nameCanonical;
    }

    /**
     * Set description
     *
     * @param string $description
     *
     * @return Offer
     */
    public function setDescription($description)
    {
        $this->description = $description;

        return $this;
    }

    /**
     * Get description
     *
     * @return string
     */
    public function getDescription()
    {
        return $this->description;
    }

    /**
     * Set date
     *
     * @param \DateTime $date
     *
     * @return Offer
     */
    public function setDate($date)
    {
        $this->date = $date;

        return $this;
    }

    /**
     * Get date
     *
     * @return \DateTime
     */
    public function getDate()
    {
        return $this->date;
    }

    /**
     * Set amount
     *
     * @param integer $amount
     *
     * @return Offer
     */
    public function setAmount($amount)
    {
        $this->amount = $amount;

        return $this;
    }

    /**
     * Get amount
     *
     * @return int
     */
    public function getAmount()
    {
        return $this->amount;
    }

    /**
     * Set handlingFee
     *
     * @param integer $handlingFee
     *
     * @return Offer
     */
    public function setHandlingFee($handlingFee)
    {
        $this->handlingFee = $handlingFee;

        return $this;
    }

    /**
     * Get handlingFee
     *
     * @return int
     */
    public function getHandlingFee()
    {
        return $this->handlingFee;
    }

    /**
     * Set partnershipNumber
     *
     * @param integer $partnershipNumber
     *
     * @return Offer
     */
    public function setPartnershipNumber($partnershipNumber)
    {
        $this->partnershipNumber = $partnershipNumber;

        return $this;
    }

    /**
     * Get partnershipNumber
     *
     * @return int
     */
    public function getPartnershipNumber()
    {
        return $this->partnershipNumber;
    }

    /**
     * Set finalDeal
     *
     * @param integer $finalDeal
     *
     * @return Offer
     */
    public function setFinalDeal($finalDeal)
    {
        $this->finalDeal = $finalDeal;

        return $this;
    }

    /**
     * Get finalDeal
     *
     * @return int
     */
    public function getFinalDeal()
    {
        return $this->finalDeal;
    }

    /**
     * Set status
     *
     * @param integer $status
     *
     * @return Offer
     */
    public function setStatus($status)
    {
        $this->status = $status;

        return $this;
    }

    /**
     * Get status
     *
     * @return int
     */
    public function getStatus()
    {
        return $this->status;
    }

    /**
     * Set signature
     *
     * @param boolean $signature
     *
     * @return Offer
     */
    public function setSignature($signature)
    {
        $this->signature = $signature;

        return $this;
    }

    /**
     * Get signature
     *
     * @return bool
     */
    public function getSignature()
    {
        return $this->signature;
    }

    /**
     * Set activity
     *
     * @param \AppBundle\Entity\Activity $activity
     *
     * @return Offer
     */
    public function setActivity(Activity $activity)
